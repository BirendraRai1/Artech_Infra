                <h3 class="service-heading">Heat Exchangers (Shell & Tube Type)</h3>
<br>

<p>A heat exchanger is a system used to  transfer heat between two or more fluids. It can be used in both cooling &amp;  heating process. They are mostly used in high-pressure and high temperature  applications like – Power Stations, Chemical Plants, Petroleum Refineries, Air  Conditioning, etc. </p>
<p> This type of exchangers consists of a  series of tubes which contains fluid that must be either heated or cooled and  the second fluid runs over the tubes that can either provide heat or absorb the  heat. The exchanger is one type of closed circuit cooling equipment.</p>
<p> The shell is made out of seamless/ERW or  rolled pipes and the tubes used are also seamless.</p>
<p> <strong>DESIGN PROCESS</strong></p>
<p>  <strong>Thermal Design</strong></p>
<ul>
  <li>Determining of heat transfer  areas.</li>
  <li>Selection of required  temperature specifications.</li>
  <li>Limiting the shell &amp; tube  side pressure drop.</li>
  <li>Setting shell &amp; tube  velocity limits.</li>
  <li>Finding heat transfer area  including fouling factor.</li>
</ul>
<p><strong>Mechanical Design</strong></p>
<ul>
  <li>Selection of TEMA Layout based  on the thermal design.</li>
  <li>Calculating the  no. of tubes along with its length, diameter,  pitch, material and layout based on the required temperature.</li>
  <li>Limiting the upper and lower  design on tube length.</li>
  <li>Finding shell side parameters –  material baffle cut, baffle spacing &amp; clearances.</li>
  <li>Verifying thermal conductivity  of tube material</li>
  <li>Setting upper and lower design  limits on shell diameter, baffle cut, baffle spacing.</li>
</ul>

